En esta clase conectaremos una aplicación en PHP con una base de datos MySQL, esto es contenido extra al proyecto del curso. Podrás encontrar el template de esta aplicación en su repositorio de GitHub.

Dentro de nuestro proyecto utilizaremos Eloquent para conectarnos a la base de datos, para ello dentro de la carpeta de nuestro proyecto vamos a correr el comando:

php composer.phar require illuminate/database